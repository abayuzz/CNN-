    # æ¨¡æ‹Ÿ30å¸§æ•°æ®
    print("æ¨¡æ‹Ÿ30å¸§è¿ç»­è¯†åˆ«è¿‡ç¨‹ï¼š")
    print("(å®é™…æ‰‹åŠ¿: çŸ³å¤´ï¼Œä½†æœ‰å™ªå£°å¹²æ‰°)")
    print()
    
    # å•å¸§é¢„æµ‹ï¼ˆæœ‰å™ªå£°ï¼‰
