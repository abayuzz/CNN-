def compare_effects():
    """
    å¯¹æ¯”æœ‰æ— æ—¶é—´å¹³æ»‘çš„æ•ˆæœ
    """
    print("\nğŸ“Š å®é™…æ•ˆæœå¯¹æ¯”")
